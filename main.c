unsigned char x_2(unsigned char state){
    unsigned char temp;
    if (state>=0x80)
    temp=(state<<1)^0x1b;
    else
    temp=(state<<1);
    return temp;
}
unsigned char x_3(unsigned char state){
    state=state^x_2(state);
    return state;
}
unsigned char x_4(unsigned char state){
    state=x_2(x_2(state));
    return state;
}
unsigned char x_8(unsigned char state){
    state=x_2(x_2(x_2(state)));
    return state;
}

